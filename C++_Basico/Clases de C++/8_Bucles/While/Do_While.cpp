#include<iostream>
using namespace std;

int main(void){

    //definir variable
    int Acendente=1, Decednete=10;

    //Do While
    do{
        
        cout<<Acendente<<endl;

        Acendente ++;

    } while (Acendente<= 10);
    do{
        
        cout<<Decednete<<endl;

        Decednete --;

    } while (Decednete >= 1);
    
    
    return 0;
}