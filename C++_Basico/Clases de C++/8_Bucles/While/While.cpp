#include<iostream>
using namespace std;

int main(void){

    // definir variables
    int condicion = 1, Invertido = 21;

    //bucles while
    while (condicion<=10){

        cout<<"Bucle "<<condicion<<endl;
        
        condicion ++;
    }
    while (Invertido>=1){
        
        cout<<"Bucle "<<Invertido<<endl;

        Invertido --;
    }
    
    

    return 0;
}